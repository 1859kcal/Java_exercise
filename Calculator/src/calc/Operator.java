package calc;

public class Operator {

	public int addition(int i1 , int i2) {
		int num = i1 + i2;
		return num;
	}

	public int subtraction(int i1 , int i2) {
		int num = i1 - i2;
		return num;
	}

	public int multiplication(int i1 , int i2) {
		int num = i1 * i2;
		return num;
	}

	public int division(int i1 , int i2) {
		int num = i1 / i2;
		return num;
	}

}
